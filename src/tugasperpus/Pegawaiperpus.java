    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        int rowIndex = Tablepegawai.getSelectedRow();
       
       if (rowIndex == -1){
           JOptionPane.showMessageDialog(null, "Pilih data yang akan di ubah");
          return;
       } 
        
        try {
           Connection koneksi = DriverManager.getConnection("jdbc:mysql://localhost/db_login","root","");
           String nip = tnip.getText();
           String nama = tpegawai.getText();
           String tempattanggallahir = ttgl.getText();
           String jeniskelamin = rjk1.isSelected() ? "Laki-laki" : "Perempuan";
           String agama = (String) cagama.getSelectedItem();
           String jabatan = (String) cjabatan.getSelectedItem();
           String alamat = talm.getText();
           String notelpon = tnotlpn.getText();
           
         
           String sql = "UPDATE db_pegawai SET Nip=?, Nama_pegawai=?, Ttgl=?, Jk=?, Agama=?, Jabatan=?, Alamat=?, No_telpon=?   WHERE Nip =?";
           PreparedStatement stat = koneksi.prepareStatement(sql);
           stat.setString(1, nip);
           stat.setString(2, nama);
           stat.setString(3, tempattanggallahir);
           stat.setString(4, jeniskelamin);
           stat.setString(5, agama);
           stat.setString(6, jabatan);
           stat.setString(7, alamat);
           stat.setString(8, notelpon);
           stat.setString(9, tabmode.getValueAt(rowIndex, 0).toString());
            
           
           int result = stat.executeUpdate();
           
            if(result == 1){
                
                JOptionPane.showMessageDialog(null, "Data berhasil diubah");
                kosong();
                tnip.requestFocus();
                datapegawai();
             } else {
                JOptionPane.showMessageDialog(null, "Gagal Mengubah");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
       }
    }//GEN-LAST:event_btneditActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
         int[] selectedRows = Tablepegawai.getSelectedRows();

    if (selectedRows.length == 0) {
        JOptionPane.showMessageDialog(null, "Pilih Data yang akan di Hapus");
        return;
    }

    // Buat koneksi dan statement di luar loop
    try {
        String sql = "DELETE FROM db_pegawai WHERE Nip=?";
        PreparedStatement stat = koneksi.prepareStatement(sql);

        // Loop untuk menghapus semua data yang dipilih
        for (int i = 0; i < selectedRows.length; i++) {
            int rowIndex = selectedRows[i];
            String nip = tabmode.getValueAt(rowIndex, 0).toString();
            stat.setString(1, nip);

            // Eksekusi perintah untuk menghapus data
            stat.executeUpdate();
        }

        // Setelah semua baris dihapus, tampilkan pesan sekali saja
        JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");

        // Perbarui tampilan tabel setelah penghapusan
        datapegawai();
        kosong();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btndeleteActionPerformed

