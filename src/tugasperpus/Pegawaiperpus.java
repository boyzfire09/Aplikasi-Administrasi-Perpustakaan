    private void bntcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntcariActionPerformed
        // TODO add your handling code here:
        String selectedItem = (String) cpencarianpegawai.getSelectedItem();
        String caripegawai = tcaripegawai.getText();
        
        
        
        Object[] Baris = {"Nip", "Nama", "Ttgl", "Jk", "Agama", "Jabatan", "Alamat", "No_telpon"};
        tabmode = new DefaultTableModel(null, Baris);
        Tablepegawai.setModel(tabmode);
        
        String sql = "SELECT * FROM db_pegawai WHERE Nip LIKE ? OR Nama_pegawai LIKE ?";
       
        try {
            PreparedStatement stat = koneksi.prepareStatement(sql);
            stat.setString(1, "%" + caripegawai + "%");
            stat.setString(2, "%" + caripegawai + "%");
            
            
            
            ResultSet hasil = stat.executeQuery();
            while(hasil.next()){
                String a = hasil.getString("Nip");
                String b = hasil.getString("Nama_pegawai");
                String c = hasil.getString("Ttgl");
                String d = hasil.getString("Jk");
                String e = hasil.getString("Agama");
                String f = hasil.getString("Jabatan");
                String g = hasil.getString("Alamat");
                String h = hasil.getString("No_telpon");
                
                String[] data={a, b, c, d, e, f, g, h};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erorr : " + e.getMessage());
        }
    }//GEN-LAST:event_bntcariActionPerformed

